
export const sumar = (numero1, numero2) => numero1 + numero2



export const restar = function(numero1, numero2){
    return numero1-numero2;
}

export function multiplicar (numero1, numero2){
    return numero1*numero2;
}

export function dividir(numero1, numero2){
    return numero1/numero2;
}

export function potencia(numero1, numero2){
    return Math.pow(numero1, numero2)
}